using System;

public class MathOperations //define my class and make it public
{

    public int addTen(int number) //method to add ten to the number
    {
        return number + 10; //returns the sum in this equation to the main program
    }

    public int timesTwo(int number) //method to multiply the number by two
    {
        return number * 2; //returns the product in this equation to the main program
    }

    public int minusFive(int number) //method to subtract 5 from the number
    {
        return number - 5; //returns the difference in this equation to the main program
    }
}

